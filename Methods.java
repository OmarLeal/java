public class Methods {

    //Methods
    /*Los metodos son un bloque de codigo que se ejecuta cuando lo mandan a llamar.
      Los metodos se usan para ejecutar ciertas acciones, tambien se conocen como "Funciones".
      Porque usar metodos? Para reusar codigo y usarlos muchas veces.  
      */
    static void myMethod(){
        System.out.println("Hola soy myMethod()");
    }
    public static void main(String[] args){
        myMethod();
        myMethod();
        myMethod();
    }
}
